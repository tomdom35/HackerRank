# Complete the howManyGames function below.
def howManyGames(p, d, m, s):
    cur_price = p
    cur_money = s
    games = 0
    while(cur_money >= cur_price):
        cur_money -= cur_price
        games += 1
        cur_price = cur_price - d if cur_price > m+d else m
    return games

if __name__ == '__main__':
    fptr = open(os.environ['OUTPUT_PATH'], 'w')

    pdms = input().split()

    p = int(pdms[0])

    d = int(pdms[1])

    m = int(pdms[2])

    s = int(pdms[3])

    answer = howManyGames(p, d, m, s)

    fptr.write(str(answer) + '\n')

    fptr.close()
